/*CUSTOMERSDB*/
CREATE OR REPLACE FUNCTION create_customers_db()
RETURNS VOID
AS $$
    DECLARE
        cid RECORD;
    BEGIN
        FOR cId IN 
        SELECT *
        FROM USERS U 
        WHERE U.type = 1
        LOOP
            INSERT INTO Customers(customerId, lastLoginDate) VALUES (cId.userId, NOW() - (random() * interval '5 years') + (random() * interval '23 hours') + (random() * interval '59 minutes') + (random() * interval '59 seconds'));
        END LOOP;
    END;

$$ LANGUAGE plpgsql;

SELECT create_customers_db();

