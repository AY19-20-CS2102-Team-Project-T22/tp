CREATE OR REPLACE FUNCTION create_riders_db()
RETURNS VOID
AS $$
    DECLARE
        mId RECORD;
    BEGIN
        FOR mId IN
        SELECT * FROM USERS U WHERE U.type = 2
        LOOP
            INSERT INTO DeliveryRiders(riderId,type) VALUES (mId.userId, FLOOR(RANDOM() * (2-1+1) + 1)::int);
